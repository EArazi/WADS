        <h1>THE SHABBAT PROJECT CHALLENGE</h1>
        
        <div style="text-align:center; margin-bottom:20px"><img src="images/shabbatukflyer1.jpg" width="645" height="380" alt="shabbatuk" /></div>
<p>Watford is very proud to be a part of the global Shabbos Project that started last year in South Africa, taking place this year on Shabbat 24th-25th October. What started as a local project is now being undertaken in hundreds of cities, and by thousands of Jews, around the world. At its heart is the beauty of Shabbos and how it can bring families together in a most unique way when observed properly.</p>
 
<div style="text-align:center;"><iframe width="560" height="315" src="//www.youtube.com/embed/xhfe-MZcCjE" frameborder="0" allowfullscreen></iframe></div>
 
<p>It is a challenge to each and every one of us to experience something that we haven’t before. It calls on us to take 25 hours out of our busy lives and dedicate them to ourselves, our families, our friends, and our communities. We all need to rest up at some point during the week and Shabbat offers us that unique opportunity to not only rest but to recharge as well.</p>
 
<p>I make this promise: if you take part in this unique event, you wont regret it! From the moment Shabbat comes in, WADS will be giving you the opportunity to take part in as many communal events as possible, including full meals for the whole of Shabbat for all the family (please click booking form below). We are very happy to have Charles Golding (formerly of TV-AM and LBC) as our guest speaker and I know that he will be an entertaining guest. There will be a full programme laid on for the children as well to ensure they also enjoy the day. Every effort is being made to ensure everyone who attends really enjoys the day.</p>
 
<p>The tag-line of the Shabbos Project is Keeping It Together and this has a double meaning: we are embarking on a project that will keep us together while at the same time accepting the challenge of keeping Shabbat, together. I am also very pleased to say that Watford will be Keeping It Together with Beijing. </p>

<p>You can also Keep It Together at home by inviting guests or a family to your Shabbos meals. You can light Shabbos candles in your home at the right time and do all that you can together with your family.</p>
 
<div style="text-align:center;"><iframe width="560" height="315" src="//www.youtube.com/embed/EvCAMzLknEQ" frameborder="0" allowfullscreen></iframe></div>
 
